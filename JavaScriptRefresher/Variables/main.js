// To declare variables in JS we use const for variables that do not change their value and
// let for variables that change their values

let firstName = 'Peter';
firstName = 'George';

// In this case the value of firstName is reassigned.
// When I declared variable firstName it had a value of Peter
// After reassigning to Gerorge, the value of the variable is now George

const PI = 3.14;

